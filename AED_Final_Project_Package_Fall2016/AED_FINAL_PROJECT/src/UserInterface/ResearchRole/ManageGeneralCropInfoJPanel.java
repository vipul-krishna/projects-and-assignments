    private void addCropDetailsJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCropDetailsJButtonActionPerformed
        // TODO add your handling code here:
        if(cropNameJTextField.getText().length()>0 && aboutCropJTextArea.getText().length()>0 && climateRequirementJTextArea.getText().length()>0 &&
                 soilJTextArea.getText().length()>0 && fertilizerManagementJTextArea.getText().length()>0 && nutrientManagementJTextArea.getText().length()>0)
        {
            String cropName = cropNameJTextField.getText();
            String aboutCrop = aboutCropJTextArea.getText();
            String climateRequirement = climateRequirementJTextArea.getText();
            String soil = soilJTextArea.getText();
            String fertilizer = fertilizerManagementJTextArea.getText();
            String nurtient = nutrientManagementJTextArea.getText();

            for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
            {
                if(organization instanceof ResearchOrganization)
                {
                    GeneralCropInfo generalCropInfo = ((ResearchOrganization) organization).getGeneralCropInfoDirectory().addGeneralCrop();
                    generalCropInfo.setCropName(cropName);
                    generalCropInfo.setAboutCrop(aboutCrop);
                    generalCropInfo.setClimateRequirement(climateRequirement);
                    generalCropInfo.setSoil(soil);
                    generalCropInfo.setFertilizerManagement(fertilizer);
                    generalCropInfo.setNurtientManagement(nurtient);
                }   
            }
            populateCropTable();
            JOptionPane.showMessageDialog(null, "Sucessfully Added");
            cropNameJTextField.setText("");
            aboutCropJTextArea.setText("");
            climateRequirementJTextArea.setText("");
            soilJTextArea.setText("");
            fertilizerManagementJTextArea.setText("");
            nutrientManagementJTextArea.setText("");
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please fill all the entries");
        }
    }//GEN-LAST:event_addCropDetailsJButtonActionPerformed

