    public void populateCropTable(){
        
        DefaultTableModel model = (DefaultTableModel) cropJTable.getModel();
        
        model.setRowCount(0);
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof ResearchOrganization)
            {
                for (GeneralCropInfo generalCropInfo :((ResearchOrganization)organization).getGeneralCropInfoDirectory().getGeneralCropInfoList())
                {
                    Object[] row = new Object[1];
                    row[0] = generalCropInfo;
                    model.addRow(row);
                }
            }
            
        }
        
    }
    
    

