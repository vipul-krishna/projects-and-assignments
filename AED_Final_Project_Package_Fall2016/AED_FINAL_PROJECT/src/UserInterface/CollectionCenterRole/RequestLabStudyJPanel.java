    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        //String message = resultTextField.getText();
        try{
        String temp = (farmLocationTempTextField.getText());
        String groundWaterDepth = (farmGroundWaterLevelTxtField.getText());
        String message = messageJTextField.getText();
        
        if(farmLocationTempTextField.getText().length()==0 || farmGroundWaterLevelTxtField.getText().length()==0){  
        JOptionPane.showMessageDialog(null, "Please Enter all the values");
        return;
        }
        
        if( !Pattern.matches("[0-9]*(\\.[0-9]{1,2})?", temp) ){
            JOptionPane.showMessageDialog(null, "Please enter a float value for Temperature");
            farmLocationTempTextField.setText("");
            return;
        }
        if(!Pattern.matches("[0-9]+", groundWaterDepth)){
            JOptionPane.showMessageDialog(null, "Please enter a integer value for ground water level");
            farmGroundWaterLevelTxtField.setText("");
            return;
        }
        
        //request.setMessage(message);
        //request.setSender(userAccount);
        request.setStatus("Pending with Lab");
        request.setMessage(message);
        request.setLocationTemerature(Float.parseFloat(temp));
        request.setLocationGroundWaterLevel(Integer.parseInt(groundWaterDepth));
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof LabOrganization){
                org = organization;
                System.out.println(org);
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
           JOptionPane.showMessageDialog(null, "Requested Successfully");
           farmLocationTempTextField.setText("");
           farmGroundWaterLevelTxtField.setText("");
           messageJTextField.setText("");
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please enter correct values");
        }
        
        
    }//GEN-LAST:event_submitJButtonActionPerformed

