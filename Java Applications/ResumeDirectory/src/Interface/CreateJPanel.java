    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        
        String name = nameTextField.getText();
        String dob = dobTextField.getText();
        String address1 = addressOneTextField.getText();
        String address2 = addressTwoTextField.getText();
        String city = cityTextField.getText();
        String country = countryTextField.getText();
        String affiliation = affiliationTextField.getText();
        String contact = contactTextField.getText();
        String careerObj = careerObjTextArea.getText();
        int passYear = Integer.parseInt(passYearTextField.getText());
        String school = schoolTextField.getText();
        String schoolCity = schoolCityTextField.getText();
        String emailId = emailTextField.getText();
        
        Resume rsm = resumeList.addResume();
        
        rsm.setName(name);
        rsm.setDob(dob);
        rsm.setAddress1(address1);
        rsm.setAddress2(address2);
        rsm.setCity(city);
        rsm.setCountry(country);
        rsm.setAffiliation(affiliation);
        rsm.setContact(contact);
        rsm.setCareerObj(careerObj);
        rsm.setDegreeYear(passYear);
        rsm.setSchool(school);
        rsm.setSchoolCity(schoolCity);
        rsm.setEmail(emailId);
        rsm.setGender(radioGender);
        rsm.setWorkExp(radioWorkExp);
        rsm.setNationality(radioNationality);
        rsm.setDegree(radioDegree);
        rsm.setProgramHistory(skillsCheckValue);
        rsm.setLanguage(languageCheckValue);
        rsm.setIcon(personImage);
        
        
        JOptionPane.showMessageDialog(null, "Resume is Created and Saved Successfully");
        
        nameTextField.setText("");
        dobTextField.setText("");
        addressOneTextField.setText("");
        addressTwoTextField.setText("");
        cityTextField.setText("");
        countryTextField.setText("");
        affiliationTextField.setText("");
        contactTextField.setText("");
        careerObjTextArea.setText("");
        passYearTextField.setText("");
        schoolCityTextField.setText("");
        schoolTextField.setText("");
        emailTextField.setText("");
        
        imageLabel.setIcon(null);
        imageLabel.revalidate();
        imagePathTextField.setText("");
        
        genderRadioGroup.clearSelection();
        workExpRadioBtnGroup.clearSelection();
        degreeRadioBtnGroup.clearSelection();
        nationalityRadioBtnGroup.clearSelection();
        radioGender = "";
        radioWorkExp = "";
        radioDegree = "";
        radioNationality = "";
        
        javaCheckBox.setSelected(false);
        cCheckBox.setSelected(false);
        jsCheckbox.setSelected(false);
        englishCheckbox.setSelected(false);
        chineseCheckbox.setSelected(false);
        spanishCheckbox.setSelected(false);
        skillsCheckValue = "";
        languageCheckValue = "";
        
    }//GEN-LAST:event_CreateBtnActionPerformed

