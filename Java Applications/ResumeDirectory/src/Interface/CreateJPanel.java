    private void browseImageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseImageBtnActionPerformed
        
    try{
        
        JFileChooser chooser=new JFileChooser();
        chooser.showOpenDialog(null);
        File f=chooser.getSelectedFile();
        
        Image image=ImageIO.read(f);
        
        String fileName=f.getAbsolutePath();
        
        if (fileName.endsWith(".jpg") || fileName.endsWith("jpeg"))
        {
        imagePathTextField.setText(fileName);
        ImageIcon icon=new ImageIcon(fileName);
        imageLabel.setIcon(new ImageIcon(image.getScaledInstance(154,127,150)));
        personImage = new ImageIcon(image.getScaledInstance(154,127,150));
        }
        
        else
        {
             getToolkit().beep();
             
             JOptionPane.showMessageDialog(null, "Please select jpg or jpeg images only");
             
             
        }
        
      }
        
        catch(Exception e){}
    }//GEN-LAST:event_browseImageBtnActionPerformed

    private void contactTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactTextFieldKeyTyped
        char pressedKey = evt.getKeyChar();
        if(!(Character.isDigit(pressedKey) || pressedKey == KeyEvent.VK_BACKSPACE || pressedKey == KeyEvent.VK_DELETE))
        {
            getToolkit().beep();
            
            JOptionPane.showMessageDialog(null, "Please enter numbers only");
            evt.consume();
            
        }
    }//GEN-LAST:event_contactTextFieldKeyTyped

