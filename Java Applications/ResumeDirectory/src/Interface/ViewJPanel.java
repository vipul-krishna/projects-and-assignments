    public void showTableDetails(){
        
        DefaultTableModel defaultTable = (DefaultTableModel)tblResumeList.getModel();
        defaultTable.setRowCount(0);
        
        for(Resume resume : resumeList.getResumeList())
        {
            Object obj[] = new Object[2];
            obj[0] = resume;
            obj[1] = resume.getEmail();
            
            defaultTable.addRow(obj);
        }
        
        
        
    }
    
    

