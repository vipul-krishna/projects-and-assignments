    private void viewSelectedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSelectedBtnActionPerformed
        int selectedRow = tblResumeList.getSelectedRow();
        
        if(selectedRow >=0)
        {
            Resume resume = (Resume)tblResumeList.getValueAt(selectedRow, 0);
            
            nameTextField.setText(resume.getName());
            addressOneTextField.setText(resume.getAddress1());
            addressTwoTextField.setText(resume.getAddress2());
            cityTextField.setText(resume.getCity());
            countryTextField.setText(resume.getCountry());
            affiliationTextField.setText(resume.getAffiliation());
            passYearTextField.setText(String.valueOf(resume.getDegreeYear()));
            schoolTextField.setText(resume.getSchool());
            schoolCityTextField.setText(resume.getSchoolCity());
            careerObjTextArea.setText(resume.getCareerObj());
            contactTextField.setText(resume.getContact());
            emailTextField.setText(resume.getEmail());
            dobTextField.setText(resume.getDob());
            genderTextField.setText(resume.getGender());
            workExpTextField.setText(resume.getWorkExp());
            degreeTextField.setText(resume.getDegree());
            nationalityTextField.setText(resume.getNationality());
            skillSetsTextField.setText(resume.getProgramHistory());
            languageTextField.setText(resume.getLanguage());
            imageLabel.setIcon(resume.getIcon());
            
            
            
        }
        
        else
            JOptionPane.showMessageDialog(null, "Please select a row from the table");
    }//GEN-LAST:event_viewSelectedBtnActionPerformed

    private void deleteSelectedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSelectedBtnActionPerformed
        int selectedRow = tblResumeList.getSelectedRow();
        
        if (selectedRow >=0)
        {
            Resume resume = (Resume)tblResumeList.getValueAt(selectedRow, 0);
            resumeList.deleteResume(resume);
            JOptionPane.showMessageDialog(null, "Selected Resume is Deleted");
            
            showTableDetails();
            
            nameTextField.setText("");
            dobTextField.setText("");
            addressOneTextField.setText("");
            addressTwoTextField.setText("");
            cityTextField.setText("");
            countryTextField.setText("");
            affiliationTextField.setText("");
            contactTextField.setText("");
            careerObjTextArea.setText("");
            passYearTextField.setText("");
            schoolCityTextField.setText("");
            schoolTextField.setText("");
            emailTextField.setText("");
            
            genderTextField.setText("");
            workExpTextField.setText("");
            degreeTextField.setText("");
            nationalityTextField.setText("");
            
            skillSetsTextField.setText("");
            languageTextField.setText("");
            
            imageLabel.setIcon(null);
            imageLabel.revalidate();
            
            
        }
        
        else
          JOptionPane.showMessageDialog(null, "Please select a row from the table");  
    }//GEN-LAST:event_deleteSelectedBtnActionPerformed


