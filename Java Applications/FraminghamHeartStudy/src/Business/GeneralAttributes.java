package Business;

/**
 *
 * @author kkgarg
 */
public class GeneralAttributes {
    
    private String smoking;
    private String drinking;
    private String diabetes;
    private double weight;
    private double height;
    private int exerciseHours;
    private int durationOfSleep;
    private double caloriesIntake;
    

    public String getSmoking() {
        return smoking;
    }

    public void setSmoking(String smoking) {
        this.smoking = smoking;
    }

    public String getDrinking() {
        return drinking;
    }

    public void setDrinking(String drinking) {
        this.drinking = drinking;
    }

    public int getExerciseHours() {
        return exerciseHours;
    }

    public void setExerciseHours(int exerciseHours) {
        this.exerciseHours = exerciseHours;
    }

    public int getDurationOfSleep() {
        return durationOfSleep;
    }

    public void setDurationOfSleep(int durationOfSleep) {
        this.durationOfSleep = durationOfSleep;
    }

    public double getCaloriesIntake() {
        return caloriesIntake;
    }

    public void setCaloriesIntake(double caloriesIntake) {
        this.caloriesIntake = caloriesIntake;
    }

    public String getDiabetes() {
        return diabetes;
    }

    public void setDiabetes(String diabetes) {
        this.diabetes = diabetes;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
    
    
    

    
    
    
    
            
}
