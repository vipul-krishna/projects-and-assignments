    private void createProductBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createProductBtnActionPerformed
       
    if(productNameTextField.getText().equals("") || productDescTextArea.getText().equals("")||productBenefitsTextArea.getText().equals(""))
        {
           
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Make Sure All Values are Entered Correctly","Warning",JOptionPane.WARNING_MESSAGE);
        }
   else{
        try{
        
        String vendorName = vendorNameTextField.getText();
        String productName = productNameTextField.getText();
        //String productType = productTypeTextField.getText();
        int productID = Integer.parseInt(productIDTextField.getText());
        String productDes = productDescTextArea.getText();
        String prodBenifits = productBenefitsTextArea.getText();
        float basePrice = Float.parseFloat(basePriceTextField.getText());
        float floorPrice = Float.parseFloat(floorPriceTextField.getText());
        float ceilingPrice = Float.parseFloat(ceilingPriceTextField.getText());
        
        Product prod = productCatalog.addProduct();
        
        prod.setVendorName(vendorName);
        prod.setProductName(productName);
        prod.setProductType(productType);
        prod.setProductID(productID);
        prod.setProductDescription(productDes);
        prod.setProductBenefits(prodBenifits);
        prod.setBasePrice(basePrice);
        prod.setFloorPrice(floorPrice);
        prod.setCeilingPrice(ceilingPrice);
        
        JOptionPane.showMessageDialog(null, "Product Added Successfully");
        
        //vendorNameTextField.setText("");
        productNameTextField.setText("");
        //productTypeTextField.setText("");
        productIDTextField.setText("");
        productDescTextArea.setText("");
        floorPriceTextField.setText("");
        basePriceTextField.setText("");
        ceilingPriceTextField.setText("");
        productBenefitsTextArea.setText("");
        productTypeRadioBtnGroup.clearSelection();
        
        }
        catch(Exception e){
         getToolkit().beep();
         JOptionPane.showMessageDialog(null, "Make Sure Entries Are Correct", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
       }  
        
    }//GEN-LAST:event_createProductBtnActionPerformed

    private void backCreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backCreateBtnActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length-1];
        VendorProductMgtJPanel managePanel = (VendorProductMgtJPanel)component;
        managePanel.populateTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backCreateBtnActionPerformed

    private void basePriceTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_basePriceTextFieldKeyTyped
        char pressedKey = evt.getKeyChar();
        if(!(Character.isDigit(pressedKey) || pressedKey == KeyEvent.VK_BACKSPACE || pressedKey == KeyEvent.VK_DELETE || pressedKey == KeyEvent.VK_PERIOD))
        {
            getToolkit().beep();
            
            JOptionPane.showMessageDialog(null, "Please Enter Valid Figures");
            evt.consume();
            
        }
    }//GEN-LAST:event_basePriceTextFieldKeyTyped

