    private void saveUpdatedProdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUpdatedProdBtnActionPerformed
       
        if(productNameTextField.getText().equals("") || productDescTextArea.getText().equals("")||productBenefitsTextArea.getText().equals(""))
        {
           
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Make Sure All Values are Entered Correctly","Warning",JOptionPane.WARNING_MESSAGE);
        }
   else{
        
        
        try{
        product.setVendorName(vendorNameTextField.getText());
        product.setProductName(productNameTextField.getText());
        product.setProductID(Integer.parseInt(productIDTextField.getText()));
        product.setProductType(productTypeTextField.getText());
        product.setProductDescription(productDescTextArea.getText());
        product.setProductBenefits(productBenefitsTextArea.getText());
        product.setFloorPrice(Float.parseFloat(floorPriceTextField.getText()));
        product.setCeilingPrice(Float.parseFloat(ceilingPriceTextField.getText()));
        product.setBasePrice(Float.parseFloat(basePriceTextField.getText()));
        
        JOptionPane.showMessageDialog(null, "Updates are saved");
        updateViewProdBtn.setEnabled(true);
        saveUpdatedProdBtn.setEnabled(false);
        saveUpdatedProdBtn.setEnabled(false);
        productNameTextField.setEditable(false);
        productTypeTextField.setEditable(false);
        productIDTextField.setEditable(false);
        productDescTextArea.setEditable(false);
        productBenefitsTextArea.setEditable(false);
        floorPriceTextField.setEditable(false);
        ceilingPriceTextField.setEditable(false);
        basePriceTextField.setEditable(false);
        }
        catch(Exception e){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Make Sure Entries Are Correct", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
      }  
        
    }//GEN-LAST:event_saveUpdatedProdBtnActionPerformed

    private void backFromViewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backFromViewBtnActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length-1];
        VendorProductMgtJPanel managePanel = (VendorProductMgtJPanel)component;
        managePanel.populateTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backFromViewBtnActionPerformed

    private void productIDTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_productIDTextFieldKeyTyped
        char pressedKey = evt.getKeyChar();
        if(!(Character.isDigit(pressedKey) || pressedKey == KeyEvent.VK_BACKSPACE || pressedKey == KeyEvent.VK_DELETE))
        {
            getToolkit().beep();
            
            JOptionPane.showMessageDialog(null, "Please Enter Numbers Only");
            evt.consume();
            
        }
    }//GEN-LAST:event_productIDTextFieldKeyTyped

