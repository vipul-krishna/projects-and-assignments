    private void productTypeTextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_productTypeTextFieldFocusLost
        String editedProductType = productTypeTextField.getText();
        if(!(editedProductType.equals("Computer") || editedProductType.equals("Printer") || editedProductType.equals("HardwareParts") || editedProductType.equals("SoftwareProducts"))){
           //JOptionPane.showMessageDialog(null, "Available Types : Computer/Printer/HardwareParts/SoftwareProducts");
           //productTypeTextField.requestFocus();
           
           String[] list = {"Computer","Printer","HardwareParts","SoftwareProducts"};
           JComboBox prodTypeBox = new JComboBox(list);
           prodTypeBox.setEditable(true);
           JOptionPane.showMessageDialog(null, prodTypeBox,"Invalid Entry. Please select any one",JOptionPane.QUESTION_MESSAGE);
           productTypeTextField.setText(prodTypeBox.getSelectedItem().toString());
        }
    }//GEN-LAST:event_productTypeTextFieldFocusLost


